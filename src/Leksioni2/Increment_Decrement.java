package Leksioni2;

public class Increment_Decrement {
    public static void main(String[] args) {
        int x=3,y=4;
        x++;
        ++y;
        int result=x+y;
        result++;
        System.out.println(++result);
        System.out.println(result++);// e afishon pastaj e inkrementon
    }
}
